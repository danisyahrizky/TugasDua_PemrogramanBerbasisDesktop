public class menu {
    String nama;
    int harga;
    String kategori;

    /**
        * Constructor untuk kelas menu
        * @param nama
        * @param harga
        * @param kategori
        */
    public menu(String nama, int harga, String kategori) {
        this.nama = nama;
        this.harga = harga;
        this.kategori = kategori;
    }
}